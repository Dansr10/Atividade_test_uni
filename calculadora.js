function soma(numero_1, numero_2){
    return numero_1 + numero_2;
}

function subtracao(numero_1, numero_2){
    return numero_1 - numero_2;
}

function multiplicacao(numero_1, numero_2){
    return numero_1 * numero_2;
}

function divisao(numero_1, numero_2){
    return numero_1 / numero_2;
}


module.exports = {soma, subtracao, divisao, multiplicacao}

